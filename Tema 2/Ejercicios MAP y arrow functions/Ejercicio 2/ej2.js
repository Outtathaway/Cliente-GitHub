let CalculaSuma = () => {
    let num1 = prompt("Introduce el primer numero");
    let num2 = prompt("Introduce el segundo numero");
    document.innerHTML = num1;
    document.innerHTML = num2;
    resultado = parseInt(num1)+parseInt(num2)
    alert(resultado);
    return resultado;
}
